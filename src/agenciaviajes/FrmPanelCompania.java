    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed

         try {
        // Obtener la fila seleccionada
        int filaSeleccionada = JTableDatos.getSelectedRow();

        // Verificar si se ha seleccionado una fila
        if (filaSeleccionada != -1) {
            // Obtener el valor de "itinerario" de la fila seleccionada
            String compania = JTableDatos.getValueAt(filaSeleccionada, 0).toString();

            // Confirmar si el usuario realmente desea eliminar el registro
            int opcion = JOptionPane.showConfirmDialog(rootPane, "¿Estás seguro de eliminar el registro?");
            
            if (opcion == JOptionPane.YES_OPTION) {
                PreparedStatement ps = cn.prepareStatement("DELETE FROM compañias WHERE id_compañia=?");
                ps.setString(1, compania);

                int indice = ps.executeUpdate();

                if (indice > 0) {
                    JOptionPane.showMessageDialog(null, "Compañía Eliminada");
                    // Actualizar el modelo de la tabla después de eliminar el cliente
                    ((DefaultTableModel) JTableDatos.getModel()).removeRow(filaSeleccionada);
                } else {
                    JOptionPane.showMessageDialog(null, "Error al eliminar compañía");
                }
            } else if (opcion == JOptionPane.NO_OPTION) {
                JOptionPane.showMessageDialog(null, "Se ha cancelado la eliminación");
            }
            
            limpiarentradas();
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila para eliminar");
        }
    } catch (HeadlessException | SQLException e) {
        System.err.println(e);
    }
    }//GEN-LAST:event_btnBorrarActionPerformed

