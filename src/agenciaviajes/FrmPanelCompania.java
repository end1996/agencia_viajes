    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        try {
            // Consulta SQL para buscar un boleto
            String sql = "SELECT id_compañia,nombre FROM compañias WHERE id_compañia=?";
            PreparedStatement ps = cn.prepareStatement(sql);

            // Establecer el valor del parámetro de búsqueda (ID Compañía)
            ps.setString(1, txtCompania.getText().trim());

            // Ejecutar la consulta
            ResultSet rs = ps.executeQuery();

            // Obtener el modelo de la tabla
            DefaultTableModel model = (DefaultTableModel) JTableDatos.getModel();

            // Limpiar el contenido anterior de la tabla
            model.setRowCount(0);

            // Verificar si se obtuvo algún resultado
            if (rs.next()) {
                // Crear un arreglo de objetos para almacenar los datos de la fila
                Object[] fila = new Object[2];
                fila[0] = rs.getString("id_compañia");
                fila[1] = rs.getString("nombre");

                // Agregar la fila al modelo de la tabla
                model.addRow(fila);

                JOptionPane.showMessageDialog(null, "Compañía encontrada.");
                limpiarentradas();
            } else {
                // Si no se encuentra una compañía con ese número, mostrar un mensaje
                JOptionPane.showMessageDialog(null, "No se encontró ninguna companía con el ID proporcionado.");
            }

            // Cerrar el ResultSet
            rs.close();

        } catch (SQLException e) {
            // Mostrar el error de manera más detallada
            JOptionPane.showMessageDialog(null, "Error al buscar los datos: " + e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error: El campo id de compañía debe ser un número válido.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

